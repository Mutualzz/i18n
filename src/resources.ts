export type AppLocale = keyof typeof resources;

export const supportedLocales = Object.keys(resources) as AppLocale[];

/** Map bare language codes to our regional catalogs when we only ship one variant. */
const languageFallbacks: Record<string, AppLocale> = {
  pt: "pt-BR",
  zh: "zh-CN",
};

/** Full-tag fallbacks for script/region variants not shipped as catalogs. */
const tagFallbacks: Record<string, AppLocale> = {
  "zh-hans": "zh-CN",
  "zh-hant": "zh-TW",
  "zh-hk": "zh-TW",
  "zh-mo": "zh-TW",
};

export function resolveLocale(
  languageTag: string | null | undefined,
): AppLocale {
  if (!languageTag) return "en";

  const normalized = languageTag.replaceAll("_", "-");
  const lower = normalized.toLowerCase();

  // Exact match (case-insensitive), e.g. pt-BR / zh-CN
  const exact = supportedLocales.find(
    (locale) => locale.toLowerCase() === lower,
  );
  if (exact) return exact;

  const tagFallback = tagFallbacks[lower];
  if (tagFallback) return tagFallback;

  const language = lower.split("-")[0] ?? lower;

  // Exact language match, e.g. es, fr, ja
  if (language in resources) {
    return language as AppLocale;
  }

  // Regional fallback, e.g. pt → pt-BR, zh → zh-CN
  const fallback = languageFallbacks[language];
  if (fallback) return fallback;

  return "en";
}

